// Switch

let nota = 5;

switch (nota) {
    case 5:
        console.log("Enhorabuena has obtenido la nota mayor");
        break;
    case 4:
        console.log("Buen trabajo pero podria mejorar");
        break;
    case 3:
        console.log("Has obtenido un suficientge");
        break;
    case 2:
        console.log("No has aprobado pero por poco");
        break;
    case 1:
        console.log("No has estudiado nada");
        break;
    default:
        console.log("Error, introduce una nota entre 1 y 5");
        break;
}
